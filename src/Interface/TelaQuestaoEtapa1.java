    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Botão remove 50%
    	int remove1, remove2;
    	
    	do {
    		remove1 = random.nextInt(0,3);
    	}while(this.perguntaAtual.getRespostas().get(remove1).isCorreta());
    	
    	do {
    		remove2 = random.nextInt(0,3);
    	}while(this.perguntaAtual.getRespostas().get(remove2).isCorreta() || remove1 == remove2);
    	
    	javax.swing.JButton botoes[] = {
    			this.jButton3,
    			this.jButton4,
    			this.jButton5,
    			this.jButton6,
    	};
    	
    	botoes[remove1].setVisible(false);
    	botoes[remove2].setVisible(false);
        jButton1.setVisible(false);
        jLabel6.setVisible(false);
    	
    	
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private void ganhou() {
        List<Pergunta> perguntas2 = ShowMilhaoIO.lerPerguntasEtapa2();
        telaSeguinte = new TelaQuestaoEtapa2(this.jogadorAtual, perguntas2, this.pontuacao, this.botaoPular);
        telaSeguinte.setVisible(true);
    	//telaGanhou = new TelaGanhou();
    	//telaGanhou.setVisible(true);
        this.pontuacao.adicionarPontos(50000);
        
        dispose();
    }
    
    private void proximaQuestao() {
    	javax.swing.JButton botoes[] = {
    			this.jButton3,
    			this.jButton4,
    			this.jButton5,
    			this.jButton6,
    	};
    	
    	for(javax.swing.JButton b : botoes) {
    		b.setVisible(true);
    	}
    	
    	this.index++;
    	this.perguntaAtual = this.perguntas.get(index);
        this.jogadorAtual.setPontuacao(this.pontuacao);
        
    	this.loadQuestionData();
    }
    
    private void errou() {
    	telaPerdeu = new TelaPerdeu(this.pontuacao);
        telaPerdeu.setVisible(true);
        ListaRanking.getInstance().addRanking(jogadorAtual);
        dispose();
    }
   
